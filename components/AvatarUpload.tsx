'use client'

import { useRef, useState } from 'react'
import { createClient } from '@/lib/supabase-browser'
import { useRouter } from 'next/navigation'

interface AvatarUploadProps {
  userId: string
  displayName: string
  currentAvatarUrl: string | null
}

function getInitials(name: string): string {
  return name
    .trim()
    .split(/\s+/)
    .map(word => word[0]?.toUpperCase() ?? '')
    .filter(Boolean)
    .slice(0, 2)
    .join('')
}

export default function AvatarUpload({ userId, displayName, currentAvatarUrl }: AvatarUploadProps) {
  const router = useRouter()
  const inputRef = useRef<HTMLInputElement>(null)
  const [avatarUrl, setAvatarUrl] = useState<string | null>(currentAvatarUrl)
  const [uploading, setUploading] = useState(false)
  const [error, setError] = useState<string | null>(null)

  const handleClick = () => {
    if (uploading) return
    inputRef.current?.click()
  }

  const handleFileChange = async (e: React.ChangeEvent<HTMLInputElement>) => {
    const file = e.target.files?.[0]
    if (!file) return

    // Validaciones
    if (!file.type.startsWith('image/')) {
      setError('Solo se permiten imágenes')
      return
    }
    if (file.size > 2 * 1024 * 1024) {
      setError('La imagen no puede superar 2 MB')
      return
    }

    setError(null)
    setUploading(true)

    try {
      const supabase = createClient()

      // Subir al bucket 'avatars' con upsert (reemplaza si ya existe)
      const { error: uploadError } = await supabase.storage
        .from('avatars')
        .upload(userId, file, { upsert: true, contentType: file.type })

      if (uploadError) throw uploadError

      // Obtener URL pública
      const { data: { publicUrl } } = supabase.storage
        .from('avatars')
        .getPublicUrl(userId)

      // Guardar en profiles.avatar_url
      // Agregamos cache-busting para forzar la recarga de la imagen
      const urlWithBust = `${publicUrl}?t=${Date.now()}`

      const { error: updateError } = await supabase
        .from('profiles')
        .update({ avatar_url: publicUrl })
        .eq('id', userId)

      if (updateError) throw updateError

      // Actualizar estado local inmediatamente
      setAvatarUrl(urlWithBust)
      router.refresh()
    } catch (err: any) {
      console.error('Error subiendo avatar:', err)
      setError('Error al subir la imagen. Intentá de nuevo.')
    } finally {
      setUploading(false)
      // Resetear input para permitir subir la misma imagen de nuevo
      if (inputRef.current) inputRef.current.value = ''
    }
  }

  const initials = getInitials(displayName || 'A')

  return (
    <div className="flex flex-col items-center gap-3">
      {/* Círculo clickeable */}
      <button
        type="button"
        onClick={handleClick}
        disabled={uploading}
        className="relative group w-24 h-24 rounded-full overflow-hidden border-2 border-purple-500/50 hover:border-purple-400 transition-all focus:outline-none focus:ring-2 focus:ring-purple-500 focus:ring-offset-2 focus:ring-offset-black disabled:cursor-not-allowed"
        aria-label="Cambiar foto de perfil"
      >
        {/* Foto o iniciales */}
        {avatarUrl ? (
          // eslint-disable-next-line @next/next/no-img-element
          <img
            src={avatarUrl}
            alt={displayName}
            className="w-full h-full object-cover"
          />
        ) : (
          <div className="w-full h-full bg-purple-900/40 flex items-center justify-center">
            <span className="text-2xl font-bold text-purple-300 select-none">
              {initials}
            </span>
          </div>
        )}

        {/* Overlay hover / loading */}
        <div className={`absolute inset-0 flex items-center justify-center transition-opacity ${
          uploading
            ? 'bg-black/60 opacity-100'
            : 'bg-black/50 opacity-0 group-hover:opacity-100'
        }`}>
          {uploading ? (
            <svg className="w-6 h-6 text-white animate-spin" fill="none" viewBox="0 0 24 24">
              <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4" />
              <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4z" />
            </svg>
          ) : (
            <svg className="w-6 h-6 text-white" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
                d="M3 9a2 2 0 012-2h.93a2 2 0 001.664-.89l.812-1.22A2 2 0 0110.07 4h3.86a2 2 0 011.664.89l.812 1.22A2 2 0 0018.07 7H19a2 2 0 012 2v9a2 2 0 01-2 2H5a2 2 0 01-2-2V9z" />
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
                d="M15 13a3 3 0 11-6 0 3 3 0 016 0z" />
            </svg>
          )}
        </div>
      </button>

      <p className="text-xs text-gray-500">
        {uploading ? 'Subiendo...' : 'Clic para cambiar foto'}
      </p>

      {error && (
        <p className="text-xs text-red-400">{error}</p>
      )}

      {/* Input file oculto */}
      <input
        ref={inputRef}
        type="file"
        accept="image/*"
        className="hidden"
        onChange={handleFileChange}
      />
    </div>
  )
}
